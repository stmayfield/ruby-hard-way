tabby_cat = "\tI'm tabbed in."
persian_cat = "I'm split\non a line."
backslash_cat = "I'm \\ a \\ cat."

# escape sequences
# \t > TAB
# \n > new line (ASCII linefeed)
# \\ > \ (single backslash)

fat_cat = """
I'll do a list:
\t* Cat food
\t* Fishies
\t* Catnup\n\t* Grass
"""

puts tabby_cat
puts persian_cat
puts backslash_cat
puts fat_cat